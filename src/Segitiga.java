public class Segitiga {
    Segitiga() {}


    public double luas(double s1, double s2) {
        return (0.5 * s1 * s2);
    }


    public double keliling(double s1, double s2, double s3) {
        return (s1 + s2 + s3);
    }

    public double tinggi(double s1, double s2) {
        return ((2 * s1) / s2);
    }

    public double alas(double s1, double s2) {
        return ((2 * s1) / s2);
    }


}
