public class Lingkaran {
    Lingkaran() {}
    private final double pi = 3.14;
    public double luas(double s1) {
        return (pi * (s1 * s1));
    }
    public double keliling(double s1) {
        return (pi * s1);
    }
    public double diameter(double s1) {
        return (2 * s1);
    }
    public double jarijari(double s1) {
        return (s1 / 2);
    }
}
