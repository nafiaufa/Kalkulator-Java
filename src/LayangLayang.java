public class LayangLayang {
    LayangLayang(){}
    public double luas(double s1 ,double s2){return (0.5 * s1 * s2);}
    public double keliling(double s1, double s2){return (s1 + s1 + s2 + s2);}
    public double d1(double s1 ,double s2){return (2 * s1 / s2);}
    public double d2(double s1 ,double s2){return (2 * s1 / s2);}
}
