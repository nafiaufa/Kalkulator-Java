public class JajarGenjang {
    JajarGenjang() {}
    public double luas(double s1, double s2) {
        return (s1 * s2);
    }
    public double keliling(double s1, double s2) {
        return (2 * (s1 + s2));
    }
    public double alas(double s1, double s2) {
        return (s1 / s2);
    }
    public double miring(double s1, double s2) {
        return ((s1 / 2) - s2);
    }
}
